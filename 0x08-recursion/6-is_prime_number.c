/**
 * is_prime_number - returns 1 if the input integer is prime number 0 if not
 * @n: number on check
 * Return: 1 divisible number and 0 if not.
 */

int is_prime_number(int n)
{
int is_prime = 0;
if (n < 2)
_is_prime = 0;
else if (n == 2)
_is_prime = 1;
else if (n % 2 == 1)
_is_prime = 1;
return (_is_prime);
}
