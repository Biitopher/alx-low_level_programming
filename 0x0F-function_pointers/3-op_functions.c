#include "3-calc.h"

/**
 * op_add - add elements
 * @a: first element
 * @b: second element
 * Return: sum of elements
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - subtracts elements
 * @a: first element
 * @b: second element
 * Return: difference of elements
 */

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiplies elements
 * @a: first element
 * @b: second element
 * Return: product of elements
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - divides elements
 * @a: first element
 * @b: second element
 * Return: division of elements
 */

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - remainder of the division of elements
 * @a: first element
 * @b: second element
 * Return: remainder of elements
 */

int op_mod(int a, int b)
{
return (a % b);
}
