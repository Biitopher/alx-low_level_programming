#include <stdio.h>

/**
 * _strncat - function that concatenates two strings
 * @dest: destination
 * @src: source
 * Return: o
*/

char *_strncat(char *dest, char *src, int n)
{
int a;
int b;
int n;
a = 0;
while (dest[a] != '\0')
{
a++;
}
b = 0;
while (src[b] != '\0')
{
a++;
b++;
}
n = '\0';
return 0;
}
