#include <stdio.h>
#include "main.h"
/**
 *print_times_table -  prints the n times table, starting with 0
 * @n : change argument
*/
void print_times_table(int n)
{
if
(n > 15)
for (n = 0; n > 15; n++)
{
printf("%d", n);
printf("\n");
{
putchar(',');
putchar(' ');
}
if (n < 0)
for (n = 0; n < 0; n--)
printf("%d", n);
printf("\n");
}
}
